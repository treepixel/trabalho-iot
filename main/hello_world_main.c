static const char EXAMPLE_ESP_WIFI_SSID[] = "Rede Wi-Fi";
static const char EXAMPLE_ESP_WIFI_PASS[] = "Senha Rede Wi-Fi";
static const int EXAMPLE_ESP_MAXIMUM_RETRY = 5;

